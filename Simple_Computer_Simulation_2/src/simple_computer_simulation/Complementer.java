/*
    Complementer
    -changes the sign of the value it holds
 */
package simple_computer_simulation;

/**
 *
 * @author bhitt
 */
public class Complementer {
    
    //Default Constructor
    Complementer(){
    }
    
    //complement function
    Integer complement(Integer value){
        value *=-1;
        return value;
    }
    
}
