/*
    Adder
    -adds the two integer values it holds, makes the result available to be
    obtained by another object
 */
package simple_computer_simulation;

/**
 *
 * @author bhitt
 */
public class Adder {
    
    //Default Constructor
    Adder(){
    }
    
    //Add function
    Integer add(Integer val1, Integer val2){
        return val1 + val2;
    }
}
